// Math object in js.

// How to round a decimal value. (4.6)

console.log(Math.round(4.6)); // 5
// Math.round returns nearest integer

console.log(Math.round(4.3)); // 4
console.log(Math.round(4.5)); // 5

// How to get nearest integer rounded up.
console.log(Math.ceil(4.3)); //5

// How to get nearest integer rounded down.
console.log(Math.floor(4.9)); //4

// Get Squroor of a number. 
console.log(Math.sqrt(49)); //7

// Max value from a colleciton of values.
console.log(Math.max(9, 10 , 8, 45, 78, 32, 21)); // 78

// Min value from a colleciton of values.
console.log(Math.min(9, 10 , 8, 45, 78, 32, 21)); // 8